def assert_subgenre_names_globally_unique() -> None:
    seen: set[str] = set()
    dupes: list[str] = []
    for names in SUBGENRES_BY_GENRE_SLUG.values():
        for n in names:
            key = n.strip().casefold()
            if key in seen:
                dupes.append(n)
            seen.add(key)
    if dupes:
        raise RuntimeError(f"Duplicate subgenre names: {dupes}")


assert_subgenre_names_globally_unique()
